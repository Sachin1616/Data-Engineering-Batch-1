# INPUT
#In Python, you can take input from the user and produce output using the input() and print() functions, respectively.

# Taking input from the user
user_input = input("Enter something: ")

# Displaying the input
print("You entered:", user_input)

# OUTPUT
# The print() function is used to display output in Python. You can print strings, variables, or a combination of both.
# Displaying output
print("Hello, World!")

# Displaying the value of a variable
name = "John"
print("My name is", name)

