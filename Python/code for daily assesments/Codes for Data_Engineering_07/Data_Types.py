# Integer
integer_variable = 5
print(f"Integer: {integer_variable}")

# Float
float_variable = 3.14
print(f"Float: {float_variable}")

# Complex
complex_variable = 2 + 3j
print(f"Complex: {complex_variable}")

# String
string_variable = "Hello, Python!"
print(f"String: {string_variable}")

# List
list_variable = [1, 2, 3, 4]
print(f"List: {list_variable}")

# Tuple
tuple_variable = (5, 6, 7)
print(f"Tuple: {tuple_variable}")

# Set
set_variable = {1, 2, 3, 3, 4}
print(f"Set: {set_variable}")

# Dictionary
dictionary_variable = {'key1': 'value1', 'key2': 'value2'}
print(f"Dictionary: {dictionary_variable}")

# Boolean
boolean_variable = True
print(f"Boolean: {boolean_variable}")

# None
none_variable = None
print(f"None: {none_variable}")

# Bytes
bytes_variable = b'binary_data'
print(f"Bytes: {bytes_variable}")

# Bytearray
bytearray_variable = bytearray(b'bytearray_data')
print(f"Bytearray: {bytearray_variable}")

# Memoryview
memoryview_variable = memoryview(b'hello')
print(f"Memoryview: {memoryview_variable}")
